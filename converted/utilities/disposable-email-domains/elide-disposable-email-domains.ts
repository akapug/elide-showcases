// Create Set for fast lookup
export const disposableDomainsSet = new Set(disposableDomains);

/**
 * Check if domain is disposable
 */
export function isDisposable(domain: string): boolean {
  return disposableDomainsSet.has(domain.toLowerCase().trim());
}

/**
 * Check if email is from disposable domain
 */
export function isDisposableEmail(email: string): boolean {
  const domain = email.split('@')[1]?.toLowerCase().trim();
  return domain ? disposableDomainsSet.has(domain) : false;
}

/**
 * Get total count of disposable domains
 */
export function getCount(): number {
  return disposableDomains.length;
}

/**
 * Export domains as JSON
 */
export function toJSON(): string {
  return JSON.stringify([...disposableDomains], null, 2);
}

/**
 * Filter email list by removing disposable emails
 */
export function filterDisposable(emails: string[]): string[] {
  return emails.filter(email => !isDisposableEmail(email));
}

export default {
  disposableDomains,
  disposableDomainsSet,
  isDisposable,
  isDisposableEmail,
  getCount,
  toJSON,
  filterDisposable
};

